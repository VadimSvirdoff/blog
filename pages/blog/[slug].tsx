import path from "path";
import matter from "gray-matter";
import Head from "next/head";
import Nav from "../../components/nav";
import Footer from "../../components/footer";
import { MDXRemote } from "next-mdx-remote";
import { serialize } from "next-mdx-remote/serialize";
import { promises as fs } from "fs";
import { GetStaticPropsContext } from "next";
import styles from "../../styles/post.module.scss";
// @ts-ignore
import prism from "@mapbox/rehype-prism";

interface Blog {
  compiledSource: string;
  scope: {
    title: string;
    slug: string;
  };
}

interface Props {
  blog: Blog;
}

export default function Post({ blog }: Props) {
  return (
    <>
      <Head>
        <title>{blog.scope.title} - Matthew Volk</title>
        <meta name="description" content="Generated by create next app" />
      </Head>
      <div className={styles.container}>
        <Nav />
        <MDXRemote {...blog} />
        <Footer />
      </div>
    </>
  );
}

export async function getStaticPaths() {
  const blogDirectory = path.join(process.cwd(), "content/blogs");
  const fileNames = await fs.readdir(blogDirectory);

  const slugs = await Promise.all(
    fileNames.map(async (filename) => {
      const filePath = path.join(blogDirectory, filename);
      const fileContent = await fs.readFile(filePath, "utf8");
      const { data } = matter(fileContent);
      const slug = `${data.slug}`;
      return slug;
    })
  );

  return {
    paths: slugs.map((slug) => ({
      params: {
        slug,
      },
    })),
    fallback: false,
  };
}

export async function getStaticProps({ params }: GetStaticPropsContext) {
  const blogDirectory = path.join(process.cwd(), "content/blogs");
  const fileNames = await fs.readdir(blogDirectory);

  const blogs = await Promise.all(
    fileNames.map(async (filename) => {
      const filePath = path.join(blogDirectory, filename);
      const fileContent = await fs.readFile(filePath, "utf8");
      const { data } = matter(fileContent);
      const slug = `${data.slug}`;
      return { slug, filePath };
    })
  );

  const match = blogs.find((blog) => {
    return blog.slug === params!.slug;
  });

  const fileContent = await fs.readFile(match!.filePath, "utf8");
  const { content, data } = matter(fileContent);
  const blog = await serialize(content, {
    scope: data,
    mdxOptions: { rehypePlugins: [prism] },
  });
  return { props: { blog } };
}
